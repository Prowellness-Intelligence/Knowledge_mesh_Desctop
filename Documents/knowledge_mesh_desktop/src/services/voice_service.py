class VoiceCommand:
    """
    Represents a voice command recognized by the system.
    
    This class provides a standardized representation of voice commands
    from various recognition engines.
    """
    
    def __init__(
        self,
        id: str,
        text: str,
        confidence: float,
        timestamp: Optional[datetime.datetime] = None,
        metadata: Optional[Dict[str, Any]] = None,
    ):
        """
        Initialize a voice command.
        
        Args:
            id: The unique identifier for the command
            text: The recognized text of the command
            confidence: The confidence level of the recognition (0.0 to 1.0)
            timestamp: The timestamp of the command
            metadata: Additional metadata for the command
        """
        self.id = id
        self.text = text
        self.confidence = confidence
        self.timestamp = timestamp or datetime.datetime.now()
        self.metadata = metadata or {}
        self.intent = None
        self.entities = []
    
    @property
    def is_confident(self) -> bool:
        """Check if the recognition confidence is high enough."""
        return self.confidence >= 0.7
    
    @property
    def is_recent(self) -> bool:
        """Check if the command is recent (less than 10 seconds old)."""
        if self.timestamp:
            return (datetime.datetime.now() - self.timestamp) < datetime.timedelta(seconds=10)
        return False
    
    def to_dict(self) -> Dict[str, Any]:
        """
        Convert the command to a dictionary.
        
        Returns:
            A dictionary representation of the command
        """
        return {
            "id": self.id,
            "text": self.text,
            "confidence": self.confidence,
            "timestamp": self.timestamp.isoformat() if self.timestamp else None,
            "metadata": self.metadata,
            "intent": self.intent,
            "entities": self.entities,
            "is_confident": self.is_confident,
            "is_recent": self.is_recent,
        }
    
    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "VoiceCommand":
        """
        Create a command from a dictionary.
        
        Args:
            data: The dictionary representation of the command
            
        Returns:
            A VoiceCommand object
        """
        timestamp = None
        if data.get("timestamp"):
            try:
                timestamp = datetime.datetime.fromisoformat(data["timestamp"])
            except ValueError:
                pass
        
        command = cls(
            id=data.get("id", ""),
            text=data.get("text", ""),
            confidence=data.get("confidence", 0.0),
            timestamp=timestamp,
            metadata=data.get("metadata", {}),
        )
        
        command.intent = data.get("intent")
        command.entities = data.get("entities", [])
        
        return command
    
    def __str__(self) -> str:
        """Get a string representation of the command."""
        return f"VoiceCommand(id={self.id}, text={self.text}, confidence={self.confidence:.2f})"
    
    def __repr__(self) -> str:
        """Get a string representation of the command."""
        return self.__str__()


class VoiceResponse:
    """
    Represents a voice response generated by the system.
    
    This class provides a standardized representation of voice responses
    for various synthesis engines.
    """
    
    def __init__(
        self,
        id: str,
        text: str,
        audio_data: Optional[bytes] = None,
        audio_format: str = "wav",
        voice_id: Optional[str] = None,
        timestamp: Optional[datetime.datetime] = None,
        metadata: Optional[Dict[str, Any]] = None,
    ):
        """
        Initialize a voice response.
        
        Args:
            id: The unique identifier for the response
            text: The text of the response
            audio_data: The audio data of the response
            audio_format: The format of the audio data
            voice_id: The ID of the voice used for synthesis
            timestamp: The timestamp of the response
            metadata: Additional metadata for the response
        """
        self.id = id
        self.text = text
        self.audio_data = audio_data
        self.audio_format = audio_format
        self.voice_id = voice_id
        self.timestamp = timestamp or datetime.datetime.now()
        self.metadata = metadata or {}
    
    @property
    def has_audio(self) -> bool:
        """Check if the response has audio data."""
        return self.audio_data is not None and len(self.audio_data) > 0
    
    @property
    def duration(self) -> Optional[float]:
        """Get the duration of the audio in seconds."""
        return self.metadata.get("duration")
    
    def to_dict(self) -> Dict[str, Any]:
        """
        Convert the response to a dictionary.
        
        Returns:
            A dictionary representation of the response
        """
        return {
            "id": self.id,
            "text": self.text,
            "audio_format": self.audio_format,
            "voice_id": self.voice_id,
            "timestamp": self.timestamp.isoformat() if self.timestamp else None,
            "metadata": self.metadata,
            "has_audio": self.has_audio,
            "duration": self.duration,
        }
    
    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "VoiceResponse":
        """
        Create a response from a dictionary.
        
        Args:
            data: The dictionary representation of the response
            
        Returns:
            A VoiceResponse object
        """
        timestamp = None
        if data.get("timestamp"):
            try:
                timestamp = datetime.datetime.fromisoformat(data["timestamp"])
            except ValueError:
                pass
        
        return cls(
            id=data.get("id", ""),
            text=data.get("text", ""),
            audio_data=None,  # Audio data is not included in the dictionary
            audio_format=data.get("audio_format", "wav"),
            voice_id=data.get("voice_id"),
            timestamp=timestamp,
            metadata=data.get("metadata", {}),
        )
    
    def save_audio(self, file_path: str) -> bool:
        """
        Save the audio data to a file.
        
        Args:
            file_path: The path to save the audio to
            
        Returns:
            True if saving was successful, False otherwise
        """
        if not self.has_audio:
            logger.warning("No audio data to save")
            return False
        
        try:
            with open(file_path, "wb") as f:
                f.write(self.audio_data)
            
            logger.info(f"Saved audio to {file_path}")
            return True
        except Exception as e:
            logger.error(f"Error saving audio to {file_path}: {e}", exc_info=True)
            return False
    
    def __str__(self) -> str:
        """Get a string representation of the response."""
        return f"VoiceResponse(id={self.id}, text={self.text}, has_audio={self.has_audio})"
    
    def __repr__(self) -> str:
        """Get a string representation of the response."""
        return self.__str__()


